
  <h1>Paris</h1>
  <p>Paris (French pronunciation: is the capital and most populous city in France,
    with an administrative-limits area of 105 square kilometres (41 square miles) and an official population of 2,206,488 (2015). </p>
  <p>The City of Paris's administrative limits form an East-West oval centred on the island at its historical heart,
    the Île de la Cité; this island is near the top of an arc of the river Seine that divides the city into southern Rive Gauche (Left Bank) and northern Rive Droite regions..</p>
